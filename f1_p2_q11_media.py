#Entrada
num1 = float(input('Insira um primeiro número:'))
num2 = float(input('Insira um segundo número: '))
num3 = float(input('Insira um terceiro número: '))

#Processamento
media = (num1 + num2 + num3) / 3

#Saída
print('A média dos números {}, {}, e {} é igual a {}'.format(num1, num2, num3, media))